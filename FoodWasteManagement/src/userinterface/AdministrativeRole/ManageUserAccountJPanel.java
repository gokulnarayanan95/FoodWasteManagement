    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        
        if((userName.equals("") || !(userName.matches("[a-zA-Z]+")))||
                (password.equals("") || !(password.matches("[a-zA-Z]+"))))
        {
            JOptionPane.showMessageDialog(null, "Invalid");
        }
        else{
        
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        if(organization.getUserAccountDirectory().checkIfUsernameIsUnique(userName)==false){
            JOptionPane.showMessageDialog(this, "Username already exists ");
            return;
        }
        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
    

        UserAccount u=organization.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
       
        popData();
        nameJTextField.setText("");
        passwordJTextField.setText("");}
    }//GEN-LAST:event_createUserJButtonActionPerformed

