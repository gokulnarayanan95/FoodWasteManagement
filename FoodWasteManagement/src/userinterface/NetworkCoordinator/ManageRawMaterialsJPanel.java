    public void populateTable() {                          //populate table
         DefaultTableModel dtm = (DefaultTableModel)tblRawMaterialList.getModel();
        dtm.setRowCount(0);
        
        try{
        for(RawMaterial rawmaterial : network.getRawMaterialcatalog().getRawMaterialList()){
            Object[] row = new Object[5];
            row[0] = rawmaterial;
            row[1] = rawmaterial.getMaterialId();
            row[2] = rawmaterial.getPrice();
            row[3] = rawmaterial.getType();

            
            dtm.addRow(row);
        }
        }catch(Exception e){
            
        }
    }

