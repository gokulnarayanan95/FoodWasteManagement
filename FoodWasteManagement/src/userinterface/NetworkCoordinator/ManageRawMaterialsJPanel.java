    private void btnSearchRawMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchRawMaterialActionPerformed
        // TODO add your handling code here:
        if(txtRawMaterialID.getText().equals("") || !(txtRawMaterialID.getText().matches("[0-9]+"))){
            JOptionPane.showMessageDialog(null,"Invalid Data");
        }
        else{
            int id= Integer.parseInt(txtRawMaterialID.getText());
            RawMaterial raw= network.getRawMaterialcatalog().searchRawMaterial(id);

            if(raw == null){
                JOptionPane.showMessageDialog(null, "RawMaterial Does Not Exist", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            else{

                SearchUpdateRawMaterialJPanel panel = new SearchUpdateRawMaterialJPanel(userProcessContainer, network, raw);
                userProcessContainer.add("SearchUpdateFoodItemPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);

                txtRawMaterialID.setText("Enter Raw Material ID Here");
            }

        }
    }//GEN-LAST:event_btnSearchRawMaterialActionPerformed

    private void btnDeleteRawMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRawMaterialActionPerformed
        // TODO add your handling code here:
        int row = tblRawMaterialList.getSelectedRow();

        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE First", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to DELETE the Material?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            RawMaterial r= (RawMaterial)tblRawMaterialList.getValueAt(row, 0);
            network.getRawMaterialcatalog().deleteRawMaterial(r);
            populateTable();
        }
    }//GEN-LAST:event_btnDeleteRawMaterialActionPerformed

    private void btnViewRawMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewRawMaterialActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblRawMaterialList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE to View Details", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            RawMaterial r= (RawMaterial)tblRawMaterialList.getValueAt(selectedRow, 0);
            SearchUpdateRawMaterialJPanel panel = new SearchUpdateRawMaterialJPanel(userProcessContainer, network, r);
            userProcessContainer.add("SearchUpdateRawMaterialJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }
    }//GEN-LAST:event_btnViewRawMaterialActionPerformed

