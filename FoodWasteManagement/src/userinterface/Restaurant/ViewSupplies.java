    public void populateTable(){      //populate the supplies table
        DefaultTableModel model=(DefaultTableModel) tb1.getModel();
        model.setRowCount(0);
        for(Ingredient i: restaurantEnterprise.getIngredientCatalog().getIngredientList()){
            Object [] row=new Object[2];
            row[0]=i.getRawMaterial();
            row[1]=i.getQuantity();
            model.addRow(row);
        }
    }
