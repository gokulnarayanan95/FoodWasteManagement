    private void btnDeleteSuppliesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSuppliesActionPerformed
        // TODO add your handling code here:
         int row = tb1.getSelectedRow();

        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please Select a Row from the TABLE First", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to DELETE the Account?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
          Ingredient i = (Ingredient)tb1.getValueAt(row, 0);
            restaurantEnterprise.getIngredientCatalog().deleteIngredient(i);
            populateTable();
        }
    }//GEN-LAST:event_btnDeleteSuppliesActionPerformed

